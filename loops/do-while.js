
// Write a program that uses a do-while loop to count down from 10 to 1.

let count = 10;

do {
    console.log(count);
    count--
} while (count >= 1);

console.log('----------------------------');


// Write a program that calculates the sum of numbers from 1 to 5 using a do-while loop.

let num = 1;
let sum = 0;

do {
    sum += num;
    num++
} while (num <= 5);

console.log(sum);


console.log('-----------------------------');

